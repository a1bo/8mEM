class CmsElementTeamResolver extends AbstractCmsElementResolver
{
    private $employeeRepository;

    public function __construct($employeeRepository)
    {
        $this->employeeRepository = $employeeRepository;
    }

    public function getType(): string
    {
        return 'team-cms-element';
    }

    public function enrich(CmsElementStruct $element, CmsPageEntity $page, SalesChannelContext $salesChannelContext, Request $request): void
    {
        $config = $element->getConfig();

        if ($config === null || $config->getValue('employees', SlotConfigField::TYPE_STATIC) === null) {
            return;
        }

        $employeeIds = $config->getValue('employees', SlotConfigField::TYPE_STATIC);

        if (empty($employeeIds)) {
            return;
        }

        $criteria = new Criteria($employeeIds);
        $criteria->addAssociation('backgroundImage');
        $criteria->addAssociation('personImage');

        /** @var EntitySearchResult $employees */
        $employees = $this->employeeRepository->search($criteria, $salesChannelContext->getContext());

        $element->setData($employees);
    }
}
